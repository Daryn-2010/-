print("=== КАЛЬКУЛЯТОР ЗАПУЩЕН ===")
print("Привет! Введите два числа, и я посчитаю!")

a = int(input("Первое число: "))
b = int(input("Второе число: "))

print()
print("РЕЗУЛЬТАТЫ:")
print(f"{a} + {b} = {a + b}")
print(f"{a} - {b} = {a - b}")
print(f"{a} * {b} = {a * b}")
if b != 0:
    print(f"{a} / {b} = {a / b}")
else:
    print("Деление на 0 невозможно!")
